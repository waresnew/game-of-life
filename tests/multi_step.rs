mod utils;
#[test]
//oscillator, period 5
fn multi_step_octagon() {
    for k in 0..5 {
        dbg!(k);
        test_solve!(
            vec![
                (0, 3),
                (1, 2),
                (2, 1),
                (3, 0),
                (3, -1),
                (2, -2),
                (1, -3),
                (0, -4),
                (-1, -4),
                (-2, -3),
                (-3, -2),
                (-4, -1),
                (-4, 0),
                (-3, 1),
                (-2, 2),
                (-1, 3),
            ],
            28 + 5 * k,
            vec![
                (-3, -2),
                (-3, 1),
                (-2, -3),
                (-2, -1),
                (-2, 0),
                (-2, 2),
                (-1, -2),
                (-1, 1),
                (0, -2),
                (0, 1),
                (1, -3),
                (1, -1),
                (1, 0),
                (1, 2),
                (2, -2),
                (2, 1)
            ]
        );
    }
}
